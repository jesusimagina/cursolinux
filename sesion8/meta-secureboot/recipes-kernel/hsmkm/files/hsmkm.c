static long hsmkm_ioctl(struct file *file, unsigned int cmd, unsigned long arg) {

    switch (cmd) {
	case IOCTL_HSMKM_GETCASIZE:
	    if (copy_to_user((int *)arg, &ca_cert_der_len, sizeof(ca_cert_der_len))) {
	        return -EFAULT;
	    }
	    break;
	case IOCTL_HSMKM_GETCA:
	    if (copy_to_user((unsigned char *)arg, ca_cert_der, ca_cert_der_len)) {
	        return -EFAULT;
	    }
	    break;
	default:
	    return -ENOTTY;
    }
    return 0;
}

